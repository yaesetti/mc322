package combat;

/**
 * Tipos de danos que podem ser aplicados
 */
public enum DamageType {
    /** Dano por impacto */
    Bludgeoning,

    /** Dano cortante */
    Slashing,

    /** Dano perfurante */
    Piercing,

    /** Dano de energia */
    Force,

    /* Dano de fogo/calor */
    Fire,

    /** Dano eletrico */
    Thunder,

    /** Dano de gelo/frio */
    Cold;
}